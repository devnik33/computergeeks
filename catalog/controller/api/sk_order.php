	public function index() {
        $this->load->model('catalog/sk_order');
		$lastRunDate = $this->request->get['lastRunDate'] ?? '1970-01-01 00:00:00';
		$results = $this->model_catalog_sk_order->getOrders([],$lastRunDate);
	
		$orders = [];
		foreach($results as $order){	
			array_push($orders,[
				'order'=>$this->model_catalog_sk_order->getOrder($order['order_id']),
				'products'=>$this->model_catalog_sk_order->getOrderProducts($order['order_id']),
				'vouchers'=>$this->model_catalog_sk_order->getOrderVouchers($order['order_id']),
				'totals'=>$this->model_catalog_sk_order->getOrderTotals($order['order_id'])
			]);
		}
		
		$this->response->addHeader('Content-Type: application/json');
		$this->response->setOutput(json_encode(['success'=>true,'data'=>$orders]) );
				
	}

	public function info($order_id) {
